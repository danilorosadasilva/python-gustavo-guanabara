#operadores aritimeticos
nome = input('Qual o seu nome?')
print('Prazer eu te conhecer {:30}!'.format(nome))
print('Prazer eu te conhecer {:>30}!'.format(nome))
print('Prazer eu te conhecer {:^30}!'.format(nome))
print('- -' * 20)
print('Prazer eu te /n conhecer {:_<30}!'.format(nome))
print('Prazer eu te conhecer {:_>30}!'.format(nome))
print('Prazer eu te conhecer {:_^30}!'.format(nome))