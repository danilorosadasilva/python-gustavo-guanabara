import random

# Sorteando 5 números entre 1 e 10 (você pode ajustar os limites conforme necessário)
numeros_sorteados = random.sample(range(1, 11), 5)

# Criando uma tupla com os números sorteados
tupla_numeros = tuple(numeros_sorteados)

# Exibindo os resultados
print("Números Sorteados:", tupla_numeros)
